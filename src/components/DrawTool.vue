<template>
  <div>
    <div class="drawtool-container">
      <el-popover
        content="Connection"
        placement="left"
        :teleported="false"
        trigger="manual"
        width="100"
        popper-class="flatmap-popper"
        :visible="hoverVisibilities[0].value"
      >
        <template #reference>
          <map-svg-icon
            icon="connection"
            class="icon-button drawConnection inactive"
            @click="$emit('dialogDisplay', true)"
            @mouseover="showTooltip(0)"
            @mouseout="hideTooltip(0)"
          />
        </template>
      </el-popover>
      <el-popover
        content="Draw Point"
        placement="top"
        :teleported="false"
        trigger="manual"
        width="80"
        popper-class="flatmap-popper"
        :visible="hoverVisibilities[1].value"
        v-if="drawnType !== 'LineString' && drawnType !== 'Polygon'"
      >
        <template #reference>
          <map-svg-icon
            icon="drawPoint"
            class="icon-button drawPoint"
            @click="drawToolEvent('Point')"
            @mouseover="showTooltip(1)"
            @mouseout="hideTooltip(1)"
          />
        </template>
      </el-popover>
      <el-popover
        content="Draw Line"
        placement="top"
        :teleported="false"
        trigger="manual"
        width="80"
        popper-class="flatmap-popper"
        :visible="hoverVisibilities[2].value"
        v-if="drawnType !== 'Point' && drawnType !== 'Polygon'"
      >
        <template #reference>
          <map-svg-icon
            icon="drawLine"
            class="icon-button drawLineString"
            @click="drawToolEvent('LineString')"
            @mouseover="showTooltip(2)"
            @mouseout="hideTooltip(2)"
          />
        </template>
      </el-popover>
      <el-popover
        content="Draw Polygon"
        placement="top"
        :teleported="false"
        trigger="manual"
        width="80"
        popper-class="flatmap-popper"
        :visible="hoverVisibilities[3].value"
        v-if="drawnType !== 'Point' && drawnType !== 'LineString'"
      >
        <template #reference>
          <map-svg-icon
            icon="drawPolygon"
            class="icon-button drawPolygon"
            @click="drawToolEvent('Polygon')"
            @mouseover="showTooltip(3)"
            @mouseout="hideTooltip(3)"
          />
        </template>
      </el-popover>
      <el-popover
        content="Delete"
        placement="top"
        :teleported="false"
        trigger="manual"
        width="80"
        popper-class="flatmap-popper"
        :visible="hoverVisibilities[4].value"
      >
        <template #reference>
          <map-svg-icon
            icon="drawTrash"
            class="icon-button drawDelete"
            @click="drawModeEvent('Delete')"
            @mouseover="showTooltip(4)"
            @mouseout="hideTooltip(4)"
          />
        </template>
      </el-popover>
      <el-popover
        content="Edit"
        placement="top"
        :teleported="false"
        trigger="manual"
        width="80"
        popper-class="flatmap-popper"
        :visible="hoverVisibilities[5].value"
      >
        <template #reference>
          <map-svg-icon
            icon="comment"
            class="icon-button drawEdit"
            @click="drawModeEvent('Edit')"
            @mouseover="showTooltip(5)"
            @mouseout="hideTooltip(5)"
          />
        </template>
      </el-popover>
    </div>
    <ConnectionDialog
      class="connection-dialog"
      v-show="connectionDisplay"
      :connectionEntry="connectionEntry"
      :inDrawing="inDrawing"
      :connection="connection"
      @dialogDisplay="$emit('dialogDisplay', $event)"
      @confirmDrawn="$emit('confirmDrawn', $event)"
      @cancelDrawn="$emit('cancelDrawn', $event)"
      @showFeatureTooltip="$emit('showFeatureTooltip', $event)"
      @hideFeatureTooltip="$emit('hideFeatureTooltip', $event)"
    />
  </div>
</template>

<script>
import { MapSvgIcon } from "@abi-software/svg-sprite";
import "@abi-software/svg-sprite/dist/style.css";
import ConnectionDialog from "./ConnectionDialog.vue";
/* eslint-disable no-alert, no-console */

/**
 * @param scopeElement    Draggable scope area (Optional)
 * @param dragElement     Draggable element
 */
const draggable = (scopeElement, dragElement) => {
  let startX, startY, clickX, clickY, posX, posY;
  // reset position in case previous pupped up dialog is dragged
  dragElement.style.left = "";
  dragElement.style.top = "";
  // const scopeRect = scopeElement.getBoundingClientRect()
  // const dragRect = dragElement.getBoundingClientRect()

  dragElement.addEventListener(
    "mousedown",
    (e) => {
      e.preventDefault();
      startX = dragElement.offsetLeft;
      startY = dragElement.offsetTop;
      clickX = e.clientX;
      clickY = e.clientY;

      dragElement.addEventListener("mousemove", drag, false);
      document.addEventListener(
        "mouseup",
        () => {
          dragElement.removeEventListener("mousemove", drag, false);
        },
        false
      );
    },
    false
  );

  function drag(e) {
    e.preventDefault();
    posX = startX - (clickX - e.clientX);
    posY = startY - (clickY - e.clientY);
    // if (
    //   (posX > scopeRect.left && ((posX + dragRect.width) < scopeRect.right)) &&
    //   (posY > scopeRect.top && ((posY + dragRect.height) < scopeRect.bottom))
    // ) {
    dragElement.style.left = `${posX}px`;
    dragElement.style.top = `${posY}px`;
    // } else {
    //   if (posX <= scopeRect.left) {
    //     dragElement.style.left = '0px';
    //   } else if (posX + dragRect.width >= scopeRect.right) {
    //     dragElement.style.left = `${scopeRect.right - dragRect.width}px`;
    //   }
    //   if (posY <= scopeRect.top) {
    //     dragElement.style.top = '0px';
    //   } else if (posY + dragRect.height >= scopeRect.bottom) {
    //     dragElement.style.top = `${scopeRect.bottom - dragRect.height}px`;
    //   }
    // }
  }
};

export default {
  name: "DrawTool",
  components: {
    MapSvgIcon,
  },
  props: {
    flatmapCanvas: undefined,
    inDrawing: {
      type: Boolean,
    },
    activeDrawTool: {
      type: String,
    },
    drawnType: {
      type: String,
    },
    drawnTypes: {
      type: Array,
    },
    activeDrawMode: {
      type: String,
    },
    drawModes: {
      type: Array,
    },
    connectionDisplay: {
      type: Boolean,
    },
    connectionEntry: {
      type: Object,
    },
    helpMode: {
      type: Boolean,
      default: false,
    },
  },
  data: function () {
    return {
      activeTool: undefined,
      activeMode: undefined,
      hoverVisibilities: [
        { value: false },
        { value: false },
        { value: false },
        { value: false },
        { value: false },
        { value: false },
      ],
      dialogPosition: {
        offsetX: 0,
        offsetY: 0,
        x: undefined,
        y: undefined,
      },
    };
  },
  computed: {
    connection: function () {
      return Object.keys(this.connectionEntry).length > 0;
    },
  },
  watch: {
    helpMode: function (newVal, oldVal) {
      if (newVal !== oldVal) {
        this.setHelpMode(newVal);
      }
    },
    activeDrawTool: function () {
      this.drawIconCssHacks();
    },
    activeDrawMode: function () {
      this.drawIconCssHacks();
    },
    connection: function (value) {
      const connectionIcon = this.$el.querySelector(".drawConnection");
      if (!value) {
        this.$emit("connection", false);
        connectionIcon.classList.add("inactive");
      } else {
        connectionIcon.classList.remove("inactive");
      }
    },
    connectionDisplay: function (display) {
      const connectionIcon = this.$el.querySelector(".drawConnection");
      if (display) {
        connectionIcon.classList.add("iconSelected");
        this.dialogCssHacks();
      } else {
        connectionIcon.classList.remove("iconSelected");
      }
      this.drawIconCssHacks();
    },
    dialogPosition: {
      handler: function () {
        const containerRect = this.$el.getBoundingClientRect();
        this.dialogPosition.offsetX = containerRect.x;
        this.dialogPosition.offsetY = containerRect.y;
      },
      deep: true,
      once: true,
    },
  },
  methods: {
    drawToolEvent: function (type) {
      if (!this.activeDrawMode && !this.connectionDisplay) {
        if (type === "Point") {
          const point = this.flatmapCanvas.querySelector(
            ".mapbox-gl-draw_point"
          );
          point.click();
          this.activeTool = point.classList.contains("active")
            ? "Point"
            : undefined;
        } else if (type === "LineString") {
          const line = this.flatmapCanvas.querySelector(".mapbox-gl-draw_line");
          line.click();
          this.activeTool = line.classList.contains("active")
            ? "LineString"
            : undefined;
        } else if (type === "Polygon") {
          const polygon = this.flatmapCanvas.querySelector(
            ".mapbox-gl-draw_polygon"
          );
          polygon.click();
          this.activeTool = polygon.classList.contains("active")
            ? "Polygon"
            : undefined;
        }
        this.$emit("drawToolbarEvent", this.activeTool);
      }
    },
    drawModeEvent: function (type) {
      if (!this.activeDrawTool) {
        this.activeMode = type;
        this.$emit("drawToolbarEvent", this.activeMode);
      }
    },
    drawIconCssHacks: function () {
      // set tool/mode icon status
      if (this.$el.querySelector(".iconSelected") || !this.connectionDisplay) {
        this.drawnTypes.map((t) => {
          const dtype = this.$el.querySelector(`.draw${t}`);
          if (dtype) {
            dtype.classList.remove("iconSelected");
            dtype.classList.remove("inactive");
          }
        });
        this.drawModes.map((m) => {
          this.$el.querySelector(`.draw${m}`).classList.remove("iconSelected");
          this.$el.querySelector(`.draw${m}`).classList.remove("inactive");
        });
      }
      if (this.activeDrawTool) {
        this.$el
          .querySelector(`.draw${this.activeDrawTool}`)
          .classList.add("iconSelected");
        this.drawModes.map((m) => {
          this.$el.querySelector(`.draw${m}`).classList.add("inactive");
        });
      } else if (this.activeDrawMode || this.connectionDisplay) {
        if (this.activeDrawMode) {
          this.$el
            .querySelector(`.draw${this.activeDrawMode}`)
            .classList.add("iconSelected");
        }
        this.drawnTypes.map((t) => {
          const dtype = this.$el.querySelector(`.draw${t}`);
          if (dtype) dtype.classList.add("inactive");
        });
      }
    },
    dialogCssHacks: function () {
      this.$nextTick(() => {
        const dialog = this.$el.querySelector(".connection-dialog");
        draggable(this.flatmapCanvas, dialog);
        // dialog popup at the click position
        // slightly change x or y if close to boundary
        let posX, posY;
        const containerRect = this.flatmapCanvas.getBoundingClientRect();
        const dialogRect = dialog.getBoundingClientRect();
        if (this.dialogPosition.x > containerRect.width / 2) {
          posX = this.dialogPosition.x - dialogRect.width;
        } else {
          posX = this.dialogPosition.x;
        }
        if (this.dialogPosition.y > containerRect.height / 2) {
          posY = this.dialogPosition.y - dialogRect.height;
        } else {
          posY = this.dialogPosition.y;
        }
        dialog.style.transform = `translate(${
          posX - this.dialogPosition.offsetX
        }px, ${posY - this.dialogPosition.offsetY}px)`;
      });
    },
    /**
     * @vuese
     * Function to show tooltip
     * by providing ``tooltipNumber``.
     * @arg tooltipNumber
     */
    showTooltip: function (tooltipNumber) {
      if (!this.inHelp) {
        clearTimeout(this.tooltipWait[tooltipNumber]);
        this.tooltipWait[tooltipNumber] = setTimeout(() => {
          this.hoverVisibilities[tooltipNumber].value = true;
        }, 500);
      }
    },
    /**
     * @vuese
     * Function to hide tooltip
     * by providing ``tooltipNumber``.
     * @arg tooltipNumber
     */
    hideTooltip: function (tooltipNumber) {
      if (!this.inHelp) {
        clearTimeout(this.tooltipWait[tooltipNumber]);
        this.tooltipWait[tooltipNumber] = setTimeout(() => {
          this.hoverVisibilities[tooltipNumber].value = false;
        }, 500);
      }
    },
    setHelpMode: function (helpMode) {
      if (helpMode) {
        this.inHelp = true;
        this.hoverVisibilities.forEach((item) => {
          item.value = true;
        });
      } else {
        this.inHelp = false;
        this.hoverVisibilities.forEach((item) => {
          item.value = false;
        });
      }
    },
  },
  mounted: function () {
    this.tooltipWait = [];
    this.tooltipWait.length = this.hoverVisibilities.length;
    this.flatmapCanvas.querySelector(".maplibregl-canvas").addEventListener(
      "click",
      (e) => {
        e.preventDefault();
        this.dialogPosition.x = e.clientX;
        this.dialogPosition.y = e.clientY;
        // use to fix the draw point pop up position issue
        if (this.activeDrawTool === "Point") {
          this.dialogCssHacks();
        }
      },
      false
    );
  },
};
</script>

